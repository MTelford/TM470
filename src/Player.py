class Player:
    def __init__(self, name='', location=''):
        self.name = name
        self.location = location
        self.player_cards = None


    def get_name(self):
        return self.location

    def get_location(self):
        return self.location

    def set_name(self, name):
        self.name = name

    def set_location(self, location):
        self.location = location